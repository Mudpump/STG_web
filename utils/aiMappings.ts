export function getRandomCategory(): { id: CategoryId, name: string } {
  const cats = CATEGORIES.filter(c => c.id !== 'ALL');
  const cat = cats[Math.floor(Math.random() * cats.length)];
  return { id: cat.id, name: cat.name };
}
